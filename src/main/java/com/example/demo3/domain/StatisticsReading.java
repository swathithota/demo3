package com.example.demo3.domain;

public class StatisticsReading {

    private String maxReading;
    private String minReading;
    private String avgReading;
    private String sumReading;

    public String getMaxReading() {
        return maxReading;
    }

    public void setMaxReading(String maxReading) {
        this.maxReading = maxReading;
    }

    public String getMinReading() {
        return minReading;
    }

    public void setMinReading(String minReading) {
        this.minReading = minReading;
    }

    public String getAvgReading() {
        return avgReading;
    }

    public void setAvgReading(String avgReading) {
        this.avgReading = avgReading;
    }


    public String getSumReading() {
        return sumReading;
    }

    public void setSumReading(String sumReading) {
        this.sumReading = sumReading;
    }
}
